function findMissingNumber(arr) {
    const n = arr.length + 1; // Длина массива должна быть на 1 больше пропущенного числа
    const totalSum = (n * (n + 1)) / 2; // Сумма всех чисел от 1 до n по формуле арифметической прогрессии
    const arraySum = arr.reduce((acc, num) => acc + num, 0); // Сумма чисел в массиве

    // Пропущенное число равно разнице между суммой всех чисел от 1 до n и суммой чисел в массиве
    const missingNumber = totalSum - arraySum;

    return missingNumber;
}

// Пример использования функции
const numbers = [1, 2, 3, 5];
const missingNum = findMissingNumber(numbers);
console.log("Пропущенное число:", missingNum);

// -- Функция findMissingNumber принимает массив arr, содержащий все целые числа от 1 до n, кроме одного. Она сначала вычисляет общую сумму всех чисел от 1 до n по формуле арифметической прогрессии. Затем находит сумму чисел в массиве. Пропущенное число - это разница между общей суммой и суммой чисел в массиве. Это число функция возвращает.