function bubbleSort(arr) {
    const len = arr.length;

    // Внешний цикл для прохода по всем элементам массива
    for (let i = 0; i < len - 1; i++) {
        // Внутренний цикл для сравнения и обмена элементов
        for (let j = 0; j < len - 1 - i; j++) {
            // Если текущий элемент больше следующего, меняем их местами
            if (arr[j] > arr[j + 1]) {
                // Обмен значений
                const temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
            }
        }
    }

    return arr;
}

// Пример использования функции
const numbers = [64, 34, 25, 12, 22, 11, 90];
const sortedNumbers = bubbleSort(numbers);
console.log("Отсортированный массив:", sortedNumbers);

// -- Этот алгоритм сначала проходит по всем элементам массива и сравнивает их попарно. Если текущий элемент больше следующего, они меняются местами. Этот процесс повторяется до тех пор, пока массив не будет отсортирован.