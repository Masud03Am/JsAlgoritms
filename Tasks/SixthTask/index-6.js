function factorial(n) {
    // Базовый случай: если n равно 0 или 1, возвращаем 1
    if (n === 0 || n === 1) {
        return 1;
    }
    // Рекурсивно вычисляем факториал числа (n - 1) и умножаем на n
    return n * factorial(n - 1);
}

// Пример использования функции
const number = 5;
const result = factorial(number);
console.log(`Факториал числа ${number} равен ${result}`);

// -- Функция factorial принимает число n. Если n равно 0 или 1, она возвращает 1 (потому что факториал 0 и 1 всегда равен 1). В противном случае, она умножает число n на результат вызова factorial для числа (n - 1)(то есть факториал для предыдущего числа).