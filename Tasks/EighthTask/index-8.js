function isPrime(number) {
    // Если число меньше или равно 1, оно не является простым
    if (number <= 1) {
        return false;
    }

    // Проверяем делители от 2 до корня из числа
    for (let i = 2; i <= Math.sqrt(number); i++) {
        // Если число делится без остатка на текущий делитель, оно не простое
        if (number % i === 0) {
            return false;
        }
    }

    // Если не найдено делителей от 2 до корня из числа, оно простое
    return true;
}

// Пример использования функции
const num = 7;
const isNumPrime = isPrime(num);
console.log(`Число ${num} ${isNumPrime ? "является" : "не является"} простым.`);

// -- Функция isPrime принимает число number. Она проверяет, является ли число меньше или равно 1. Если да, то оно не является простым. Затем она проверяет делители от 2 до корня из числа. Если число делится без остатка на любой из этих делителей, оно не является простым. Если не найдено делителей, функция возвращает true, иначе - false.